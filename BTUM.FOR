	PROGRAM	BTUM
C
C
C	REVISION IM800136 WJB 04-05-2011 ADDED COMPRESSION %<300
C
	IMPLICIT NONE
C
	INTEGER*4	I,J,K,L
	INTEGER*4	UFO_OPEN
C
	CHARACTER*23	DATETIME
	CHARACTER*12	DATEX/'            '/
	CHARACTER*12	DATX(100)
C
	REAL*8		PHY(16)
	REAL*8		BT2(8,100),BT3(8,100)
C
	EXTERNAL	UFO_OPEN
C
	OPEN	(UNIT=1,FILE='OREM:BTUM.DAT',STATUS='OLD',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=35,SHARED,USEROPEN=UFO_OPEN)
C
	DO 100	I=1,100
		READ	(1,REC=I)DATX(I),(BT2(J,I),J=1,8),(BT3(K,I),K=1,8)
100	CONTINUE
C
	CALL	LIB$DATE_TIME(DATETIME)	!GET TIME AND DATE
	DATEX(1:11)=DATETIME(1:11)
	IF (DATETIME(13:14) .EQ. '06')DATEX(12:12)='1'
	IF (DATETIME(13:14) .EQ. '14')DATEX(12:12)='2'
	IF (DATETIME(13:14) .EQ. '22')DATEX(12:12)='3'
	IF (DATEX(12:12) .EQ. ' ')GOTO 99999	!IF NOT END OF SHIFT QUIT
C
	IF (DATEX .EQ. DATX(1))GOTO 99999	!IF SHIFT ALREADY BUILT QUIT
C
	WRITE	(1,REC=1)DATEX,PHY	!INSERT NEW SHIFT AT TOP OF FILE
C
	DO 200	I=1,99
		WRITE	(1,REC=I+1)DATX(I),(BT2(J,I),J=1,8),(BT3(K,I),K=1,8)
200	CONTINUE
C
99999	CONTINUE
C
	CLOSE	(UNIT=1)
C
	CALL	EXIT
C
	END	

