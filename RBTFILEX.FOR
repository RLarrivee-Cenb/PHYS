C
	IMPLICIT NONE
C
	INTEGER*4	I,J,K,L
C
	REAL*8		PHY2(3),PHY3(5)
C
	CHARACTER*6	LABEL(9)/'BT+1/2','BT+1/4','BT+916','AT+1/4',
	1		         'AT-28M','  COMP',' %<200',
	1			 ' %<300','  TONS'/
C
	OPEN	(UNIT=1,FILE='OREM:BTUMX.DAT',STATUS='OLD',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=16)
