	READ	(1,REC=I)PHY2,PHY3
C
	WRITE	(6,100)LABEL(I),PHY2,LABEL(I),PHY3
100	FORMAT	(' ', A,3F8.2,2X,A,5F8.2)
C
500	CONTINUE
C
	CLOSE	(UNIT=1)
C
	CALL	EXIT
C
	END	

