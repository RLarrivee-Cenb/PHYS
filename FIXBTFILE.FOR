	PROGRAM	FIXBTFILE
C
C
C	REVISION IM800136 WJB 4-5-2011 ADDED COMPRESSIONS 
C
	IMPLICIT NONE
C
	INTEGER*4	I,J,K,L,LEN
	INTEGER*4	FLAG/64/
C
	CHARACTER*12	DATEX/'            '/
C
	REAL*8		PHY(16),C2,C3
C
	OPEN	(UNIT=6,FILE='SYS$COMMAND',STATUS='UNKNOWN')
C
	CALL	LIB$SPAWN('SET TERM/WIDTH=132',,,FLAG)
C
	OPEN	(UNIT=1,FILE='OREM:BTUM.DAT',STATUS='OLD',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=35)
C
33	CONTINUE
C
	WRITE	(6,44)
44	FORMAT	(' ','TYPE RECORD TO CHANGE')
C
	READ	(6,55)LEN,I
55	FORMAT	(Q,I3)
C
	IF (LEN .EQ. 0)GOTO 999
	IF (I .GT. 100)GOTO 999
	IF (I .LT. 1)GOTO 999
C
		READ	(1,REC=I)DATEX,PHY
C
			WRITE	(6,99)
99			FORMAT	(' ',39X,'STEP2',42X,'STEP3'/,
	1			 ' ',5X,'DATE  SHIFT  BT+1/2 BT+1/4',
	1			 1X,'BT+4M AT+1/4 AT-28M  COMP %<200 %<300',
	1			 2X,'BT+1/2 BT+1/4 BT+4M AT+1/4 AT-28M',
	1			 2X,'COMP %<200 %<300'/)
C
		WRITE	(6,150)I,DATEX,PHY
150		FORMAT	(' ',I4,1X,A,2F7.1,F6.1,2F7.1,F6.0,2F6.1,1X,2F7.1,
	1			F6.1,2F7.1,F6.0,2F6.1)
C
	WRITE	(6,222)
222	FORMAT	(' ','TYPE NEW STEP 2 COMP')
C
	READ	(6,223)LEN,C2
223	FORMAT	(Q,F6.2)
C
	IF (LEN .EQ. 0)GOTO 300
C
	PHY(4)=C2
C
	WRITE	(1,REC=I)DATEX,PHY
C
300	CONTINUE
C
	WRITE	(6,333)
333	FORMAT	(' ','TYPE NEW STEP 3 COMP')
C
	READ	(6,334)LEN,C3
334	FORMAT	(Q,F6.2)
C
	IF (LEN .EQ. 0)GOTO 400
C
	PHY(9)=C3
C
	WRITE	(1,REC=I)DATEX,PHY
C
400	CONTINUE
C
		READ	(1,REC=I)DATEX,PHY
C
			WRITE	(6,99)
C
		WRITE	(6,150)I,DATEX,PHY
C
	GOTO 33
C
999	CONTINUE
C
	CLOSE	(UNIT=1)
C
	CALL	LIB$SPAWN('SET TERM/WIDTH=80',,,FLAG)
C
	CALL	EXIT
C
	END	

