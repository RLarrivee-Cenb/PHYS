C
	PROGRAM	STRENDFILE	!CREATES THE STREND.DAT DATA FILE
C
	IMPLICIT NONE
C
	INTEGER*4	I,J,K,L
C
	CHARACTER*12	DATEX/'            '/
C
	REAL*4		VALUES(24)
C
	OPEN	(UNIT=1,FILE='LOGS:STREND.DAT',STATUS='NEW',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=27)
C
	DO 100	I=1,100
		WRITE	(1,REC=I)DATEX,VALUES
100	CONTINUE
C
	CLOSE	(UNIT=1)
C
	CALL	EXIT
C
	END	
