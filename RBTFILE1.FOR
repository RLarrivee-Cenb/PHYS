C
	PROGRAM	RBTFILE1
C
	IMPLICIT NONE
C
	INTEGER*4	I,J,K,L
	INTEGER*4	FLAG/64/
C
	CHARACTER*12	DATEX/'            '/
	CHARACTER*4	PRINTON/' [5i'/
	CHARACTER*4	PRINTOF/' [4i'/
	CHARACTER*1	FF
C
	REAL*8		PHY(14)
C
	FF=CHAR(12)
	PRINTON(1:1)=CHAR(27)
	PRINTOF(1:1)=CHAR(27)
C
	OPEN	(UNIT=6,FILE='SYS$COMMAND',STATUS='UNKNOWN',RECL=132)
C
	CALL	LIB$SPAWN('SET TERM/WIDTH=132',,,FLAG)
	CALL	LIB$SPAWN('SET TERM/FORM',,,FLAG)
C
C	WRITE	(6,180)PRINTON		!LIST GOES TO PRINTER
180	FORMAT	(' ',A)
C
C
	OPEN	(UNIT=1,FILE='OREM:BTUM.DAT',STATUS='OLD',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=31)
C
	DO 100	I=1,100
		READ	(1,REC=I)DATEX,PHY
		IF (I .EQ. 1)THEN
			WRITE	(6,99)FF
99			FORMAT	(' ',A,39X,'STEP2',42X,'STEP3'/,
	1			 ' ',5X,'DATE  SHIFT  BT+1/2 BT+1/4',
	1			 1X,'BT+4M AT+1/4 AT-28M  COMP %<200',
	1			 2X,'BT+1/2 BT+1/4 BT+4M AT+1/4 AT-28M',
	1			 2X,'COMP %<200'/)
C
		END IF
C
		IF (I .EQ. 51)THEN
			WRITE	(6,99)FF
		END IF
C
		WRITE	(6,150)I,DATEX,PHY
150		FORMAT	(' ',I4,1X,A,2F7.1,F6.1,2F7.1,F6.0,F6.1,1X,2F7.1,
	1			F6.1,2F7.1,F6.0,F6.1)
100	CONTINUE
C
	CLOSE	(UNIT=1)
C
C	WRITE	(6,180)PRINTOF		!RETURN TO SCREEN
C
	CALL	LIB$SPAWN('SET TERM/WIDTH=80',,,FLAG)
C
	CALL	EXIT
C
	END	

