C
	PROGRAM	MOVEBTUMX
C
C	MOVES THE BTUMX.DAT FILE TO BTUMZ.DAT TO MAKE ROOM FOR %<300
	IMPLICIT NONE
C
	INTEGER*4	I,J,K,L
C
	REAL*8		BT122(3),BT123(5)
	REAL*8		BT142(3),BT143(5)
	REAL*8		BT4M2(3),BT4M3(5)
	REAL*8		AT142(3),AT143(5)
	REAL*8		AT28M2(3),AT28M3(5)
	REAL*8		COMP2(3),COMP3(5)
	REAL*8		LCOMP2(3),LCOMP3(5)
	REAL*8		LCMP23(3),LCMP33(5)
	REAL*8		TONS2(3),TONS3(5)
C
	OPEN	(UNIT=1,FILE='OREM:BTUMX.DAT',STATUS='OLD',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=16)
C
	OPEN	(UNIT=3,FILE='OREM:BTUMZ.DAT',STATUS='OLD',
	1	 ACCESS='DIRECT',RECORDTYPE='FIXED',
	1	 FORM='UNFORMATTED',RECL=16)
C
	READ	(1,REC=1)BT122,BT123
	READ	(1,REC=2)BT142,BT143
	READ	(1,REC=3)BT4M2,BT4M3
	READ	(1,REC=4)AT142,AT143
	READ	(1,REC=5)AT28M2,AT28M3
	READ	(1,REC=6)COMP2,COMP3
	READ	(1,REC=7)LCOMP2,LCOMP3
	READ	(1,REC=8)TONS2,TONS3
C
	WRITE	(3,REC=1)BT122,BT123
	WRITE	(3,REC=2)BT142,BT143
	WRITE	(3,REC=3)BT4M2,BT4M3
	WRITE	(3,REC=4)AT142,AT143
	WRITE	(3,REC=5)AT28M2,AT28M3
	WRITE	(3,REC=6)COMP2,COMP3
	WRITE	(3,REC=7)LCOMP2,LCOMP3
	WRITE	(3,REC=8)LCMP23,LCMP33
	WRITE	(3,REC=9)TONS2,TONS3
C
	CLOSE	(UNIT=1)
	CLOSE	(UNIT=3)
C
	CALL	EXIT
C
	END	

